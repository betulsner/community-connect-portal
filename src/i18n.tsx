const translations: Record<SupportedLanguage, Record<string, string>> = {
  English: english,
  Chinese: chinese,
  Polish: polish,
  Persian: persian,
  Arabic: arabic,
  French: french,
  Somali: somali,
  Punjabi: punjabi,
  Spanish: spanish,
  Kurdish: kurdish,
  Dutch: dutch,
  Turkish: turkish
};

const I18nContext = createContext<{ language: SupportedLanguage; t: (key: string, fallback?: string) => string }>({
  language: "English",
  t: (key, fallback) => english[key] ?? fallback ?? key
});

export function isSupportedLanguage(language: string): language is SupportedLanguage {
  return supportedLanguages.includes(language as SupportedLanguage);
}

export function I18nProvider({ language, children }: { language: string; children: ReactNode }) {
  const safeLanguage = isSupportedLanguage(language) ? language : "English";
  const active = translations[safeLanguage];

  const t = (key: string, fallback?: string) => active[key] ?? english[key] ?? fallback ?? key;

  return <I18nContext.Provider value={{ language: safeLanguage, t }}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}
